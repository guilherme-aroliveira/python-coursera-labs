"""
Methods for mathemical operations
"""


def summation(a, b):
    """
    Method to add two numbers
    """
    result = a + b
    return result


def subtraction(a, b):
    """
    Method to subtract two numbers
    """
    result = a - b
    return result


def multiplication(a, b):
    """
    Method to multiply two numbers
    """
    result = a * b
    return result
